// в функции divide, если второй параметр равен 0, то осуществляется вызов panic("Division by zero!").
// В функции main в вызове fmt.Println(divide(4, 0)) будет выполняться оператор panic,
// поскольку второй параметр функции divide равен 0. И в этом случае все последующие операции,
// которые идут после этого вызова, например, в данном случае это вызов fmt.Println("Program has been finished"),
// не будут выполняться. В этом случае мы получим следующий консольный вывод:

